//boolean & comparison operator
let num1 = 7;
let num2 = 7;

console.log(num1 <= num2);


// == vs ===
let num3 = "7"
console.log(num1 == num3)
//== checks value but not datatype
console.log(num1 === num3)
// === checks value as well as datatype

// != vs !==
//!= doesnt check datatype but checks value only
console.log(num1 != num2);
console.log(num1 !== num3);
console.log(num1 != num3);